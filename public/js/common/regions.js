const regionTag = document.getElementById("regions");
const subRegionTag = document.getElementById("sub-regions");

// 지역 정보 추가
Object.keys(regions).forEach((region) => {
  const option = createOpntionTag(region);
  regionTag.appendChild(option);
});

// 하위 지역 정보 재설정
regionTag.addEventListener("change", function () {
  const selectedRegion = this.value;
  subRegionTag.innerHTML = "";

  if (selectedRegion && regions[selectedRegion]) {
    let defaultSubRegionOption = createOpntionTag("세부 지역 선택");
    subRegionTag.appendChild(defaultSubRegionOption);

    regions[selectedRegion].forEach((subRegion) => {
      const option = createOpntionTag(subRegion);
      subRegionTag.appendChild(option);
    });
  }
});


function createOpntionTag(name) {
  const option = document.createElement("option");
  option.value = name;
  option.textContent = name;
  return option;
}